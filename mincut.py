class UndirectedGraph(object):
  def __init__(self):
    self.vertices = []
    self.edges = {}
    self.children = {}
  def add_vertex(self, v):
    self.vertices.append(v)
    self.edges[(v,v)] = 0
  def add_vertices(self, v_s):
    self.vertices.extend(v_s)
    for v in v_s:
      self.edges[(v,v)] = 0
  def add_edge(self, u, v, w=1):
    self.edges[(u,v)] = w
    self.edges[(v,u)] = w
  def neighbors(self, u):
    return [v for v in self.vertices if (u,v) in self.edges]
  def adjacents(self, u):
    li = []
    for v in self.vertices:
      w = self.edges.get((u,v),0)
      if w:
        li.append((v,w))
    return li
  def merge(self, u, v):
    self.children[v] |= self.children[u]
    u_adjs = self.adjacents(u)
    for z,w in u_adjs:
      self.edges.pop((u,z))
      self.edges.pop((z,u))
    self.vertices.remove(u)
    v_adjs = dict(self.adjacents(v))
    for n, w in u_adjs:
      if n == v:
        continue
      v_adjs[n] = v_adjs.get(n,0) + w
    for n, w in v_adjs.iteritems():
      self.edges[(v,n)] = w
      self.edges[(n,v)] = w    

def w(g, A, y):
  assert y not in A
  return sum(w for v, w in g.adjacents(y) if v in A)

def min_cut_phase(g, a):
  A = set([a])
  V = set(g.vertices)
  order = [a]
  while A != V:
    w_candidates = [(w(g, A, v), v) for v in V-A]  #: candidates for most tightly connected
    _, x = max(w_candidates)
    A.add(x)
    order.append(x)
  t, s = order[-1], order[-2]
  min_cut = g.children[t]
  min_cut_val = sum(w for v, w in g.adjacents(t))
  g.merge(t, s)
  return min_cut, min_cut_val

def minimum_cut(g, a):
  V = set(g.vertices)
  for v in V:
      g.children[v] = set([v])
  min_cut = {a}
  min_cut_val = sum(w for v, w in g.adjacents(a))
  while len(g.vertices) > 1:
    min_cut_candidate, candval = min_cut_phase(g, a)
    if candval < min_cut_val:
      min_cut = min_cut_candidate
      min_cut_val = candval
  return min_cut, V-min_cut, min_cut_val


